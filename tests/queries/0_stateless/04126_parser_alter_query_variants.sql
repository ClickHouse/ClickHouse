-- Exercise ParserAlterQuery command variants that don't have dedicated tests:
-- MODIFY SQL SECURITY / DEFINER, MODIFY REFRESH, MODIFY COMMENT, APPLY DELETED MASK,
-- MOVE PARTITION to DISK/VOLUME/TABLE, FETCH PARTITION, REPLACE PARTITION,
-- FREEZE/UNFREEZE, MODIFY/RESET SETTING, CLEAR COLUMN/INDEX.

SELECT '--- MODIFY SQL SECURITY ---';
SELECT formatQuery('ALTER TABLE db.t MODIFY SQL SECURITY DEFINER');
SELECT formatQuery('ALTER TABLE db.t MODIFY SQL SECURITY INVOKER');
SELECT formatQuery('ALTER TABLE db.t MODIFY SQL SECURITY NONE');

SELECT '--- MODIFY DEFINER ---';
SELECT formatQuery('ALTER TABLE db.t MODIFY DEFINER = CURRENT_USER');

SELECT '--- MODIFY REFRESH ---';
SELECT formatQuery('ALTER TABLE db.t MODIFY REFRESH EVERY 1 MINUTE');

SELECT '--- MODIFY COMMENT ---';
SELECT formatQuery('ALTER TABLE db.t MODIFY COMMENT ''new comment''');

SELECT '--- APPLY DELETED MASK ---';
SELECT formatQuery('ALTER TABLE db.t APPLY DELETED MASK');
SELECT formatQuery('ALTER TABLE db.t APPLY DELETED MASK IN PARTITION ID ''20230101''');
SELECT formatQuery('ALTER TABLE db.t APPLY DELETED MASK IN PARTITION 1');

SELECT '--- MOVE PARTITION ---';
SELECT formatQuery('ALTER TABLE db.t MOVE PARTITION 1 TO DISK ''mydisk''');
SELECT formatQuery('ALTER TABLE db.t MOVE PARTITION 1 TO VOLUME ''myvol''');
SELECT formatQuery('ALTER TABLE db.t MOVE PARTITION 1 TO TABLE db.u');
SELECT formatQuery('ALTER TABLE db.t MOVE PART ''part_1_1_0'' TO DISK ''mydisk''');

SELECT '--- FETCH / REPLACE / ATTACH PARTITION ---';
SELECT formatQuery('ALTER TABLE db.t FETCH PARTITION 1 FROM ''/from''');
SELECT formatQuery('ALTER TABLE db.t FETCH PART ''x'' FROM ''/from''');
SELECT formatQuery('ALTER TABLE db.t REPLACE PARTITION 1 FROM db.u');
SELECT formatQuery('ALTER TABLE db.t ATTACH PARTITION 1 FROM db.u');
SELECT formatQuery('ALTER TABLE db.t ATTACH PART ''part_1_1_0''');

SELECT '--- DROP/DETACH PARTITION variants ---';
SELECT formatQuery('ALTER TABLE db.t DROP PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t DROP DETACHED PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t DROP PART ''part_1_1_0''');
SELECT formatQuery('ALTER TABLE db.t DETACH PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t DETACH PART ''part_1_1_0''');
SELECT formatQuery('ALTER TABLE db.t FORGET PARTITION 1');

SELECT '--- FREEZE / UNFREEZE ---';
SELECT formatQuery('ALTER TABLE db.t FREEZE');
SELECT formatQuery('ALTER TABLE db.t FREEZE PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t FREEZE PARTITION 1 WITH NAME ''snap''');
SELECT formatQuery('ALTER TABLE db.t UNFREEZE WITH NAME ''snap''');
SELECT formatQuery('ALTER TABLE db.t UNFREEZE PARTITION 1 WITH NAME ''snap''');

SELECT '--- MODIFY / RESET SETTING ---';
SELECT formatQuery('ALTER TABLE db.t MODIFY SETTING index_granularity = 128');
SELECT formatQuery('ALTER TABLE db.t MODIFY SETTING index_granularity = 128, max_parts_in_total = 1000');
SELECT formatQuery('ALTER TABLE db.t RESET SETTING index_granularity');
SELECT formatQuery('ALTER TABLE db.t RESET SETTING index_granularity, max_parts_in_total');

SELECT '--- CLEAR COLUMN / CLEAR INDEX ---';
SELECT formatQuery('ALTER TABLE db.t CLEAR COLUMN c IN PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t CLEAR COLUMN IF EXISTS c IN PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t CLEAR INDEX idx IN PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t CLEAR INDEX IF EXISTS idx IN PARTITION 1');

SELECT '--- MATERIALIZE INDEX / PROJECTION / STATISTICS / COLUMN ---';
SELECT formatQuery('ALTER TABLE db.t MATERIALIZE INDEX idx');
SELECT formatQuery('ALTER TABLE db.t MATERIALIZE INDEX idx IN PARTITION 1');
SELECT formatQuery('ALTER TABLE db.t MATERIALIZE PROJECTION prj');
SELECT formatQuery('ALTER TABLE db.t MATERIALIZE STATISTICS c');
SELECT formatQuery('ALTER TABLE db.t MATERIALIZE COLUMN c');

SELECT '--- ADD / DROP / CLEAR PROJECTION ---';
SELECT formatQuery('ALTER TABLE db.t ADD PROJECTION prj (SELECT * ORDER BY a)');
SELECT formatQuery('ALTER TABLE db.t ADD PROJECTION IF NOT EXISTS prj (SELECT * ORDER BY a)');
SELECT formatQuery('ALTER TABLE db.t DROP PROJECTION prj');
SELECT formatQuery('ALTER TABLE db.t DROP PROJECTION IF EXISTS prj');
SELECT formatQuery('ALTER TABLE db.t CLEAR PROJECTION prj IN PARTITION 1');

SELECT '--- RENAME / MODIFY COLUMN ---';
SELECT formatQuery('ALTER TABLE db.t RENAME COLUMN a TO b');
SELECT formatQuery('ALTER TABLE db.t RENAME COLUMN IF EXISTS a TO b');
SELECT formatQuery('ALTER TABLE db.t MODIFY COLUMN c Int32');
SELECT formatQuery('ALTER TABLE db.t MODIFY COLUMN c Int32 DEFAULT 0');
SELECT formatQuery('ALTER TABLE db.t MODIFY COLUMN c REMOVE DEFAULT');
SELECT formatQuery('ALTER TABLE db.t MODIFY COLUMN c MODIFY SETTING min_compress_block_size = 1024');
SELECT formatQuery('ALTER TABLE db.t MODIFY COLUMN c RESET SETTING min_compress_block_size');
