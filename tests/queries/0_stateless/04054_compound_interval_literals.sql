-- Mixed-type interval literals: INTERVAL 'string' FROM_KIND TO TO_KIND
-- https://github.com/ClickHouse/ClickHouse/issues/99611

SELECT '-- HOUR TO MINUTE';
SELECT INTERVAL '1:30' HOUR TO MINUTE;
SELECT toTypeName(INTERVAL '1:30' HOUR TO MINUTE);
SELECT INTERVAL '1:60' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:100' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:30' MINUTE TO HOUR; -- { clientError SYNTAX_ERROR }

SELECT '-- HOUR TO SECOND';
SELECT INTERVAL '1:30:45' HOUR TO SECOND;
SELECT toTypeName(INTERVAL '1:30:45' HOUR TO SECOND);
SELECT INTERVAL '1:60:00' HOUR TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:00:60' HOUR TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:30:45' SECOND TO HOUR; -- { clientError SYNTAX_ERROR }

SELECT '-- MINUTE TO SECOND';
SELECT INTERVAL '5:30' MINUTE TO SECOND;
SELECT toTypeName(INTERVAL '5:30' MINUTE TO SECOND);
SELECT INTERVAL '5:60' MINUTE TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5:30' SECOND TO MINUTE; -- { clientError SYNTAX_ERROR }

SELECT '-- YEAR TO MONTH';
SELECT INTERVAL '2-6' YEAR TO MONTH;
SELECT toTypeName(INTERVAL '2-6' YEAR TO MONTH);
SELECT INTERVAL '2-12' YEAR TO MONTH; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '2-13' YEAR TO MONTH; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '2-6' MONTH TO YEAR; -- { clientError SYNTAX_ERROR }

SELECT '-- DAY TO HOUR';
SELECT INTERVAL '5 12' DAY TO HOUR;
SELECT toTypeName(INTERVAL '5 12' DAY TO HOUR);
SELECT INTERVAL '5 24' DAY TO HOUR; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5 12' HOUR TO DAY; -- { clientError SYNTAX_ERROR }

SELECT '-- DAY TO MINUTE';
SELECT INTERVAL '5 12:30' DAY TO MINUTE;
SELECT toTypeName(INTERVAL '5 12:30' DAY TO MINUTE);
SELECT INTERVAL '5 24:00' DAY TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5 12:60' DAY TO MINUTE; -- { clientError SYNTAX_ERROR }

SELECT '-- DAY TO SECOND';
SELECT INTERVAL '5 12:30:45' DAY TO SECOND;
SELECT toTypeName(INTERVAL '5 12:30:45' DAY TO SECOND);
SELECT INTERVAL '5 24:00:00' DAY TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5 12:60:00' DAY TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5 12:30:60' DAY TO SECOND; -- { clientError SYNTAX_ERROR }

SELECT '-- Invalid: cross-group mixing';
SELECT INTERVAL '1:30' YEAR TO HOUR; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:30' YEAR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:30' YEAR TO SECOND; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1-6' DAY TO MONTH; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:30' MONTH TO MINUTE; -- { clientError SYNTAX_ERROR }

SELECT '-- Invalid: same kind';
SELECT INTERVAL '1' HOUR TO HOUR; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1' YEAR TO YEAR; -- { clientError SYNTAX_ERROR }

SELECT '-- Invalid: malformed strings';
SELECT INTERVAL '1-2-3' YEAR TO MONTH; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL 'abc' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL ':30' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }

SELECT '-- Invalid: sign on non-leading component';
SELECT INTERVAL '1:-30' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '5 -12:30' DAY TO MINUTE; -- { clientError SYNTAX_ERROR }
SELECT INTERVAL '1:+30' HOUR TO MINUTE; -- { clientError SYNTAX_ERROR }

SELECT '-- Zero values';
SELECT INTERVAL '0:00' HOUR TO MINUTE;
SELECT INTERVAL '0-0' YEAR TO MONTH;

SELECT '-- Date arithmetic';
SELECT toDateTime('2024-01-01 00:00:00') + INTERVAL '1:30' HOUR TO MINUTE;
SELECT toDate('2024-03-01') + INTERVAL '2-6' YEAR TO MONTH;
SELECT toDateTime('2024-01-01 00:00:00') + INTERVAL '5 12:30:45' DAY TO SECOND;

SELECT '-- Negative intervals';
SELECT INTERVAL '-1:30' HOUR TO MINUTE;
SELECT INTERVAL '-2-6' YEAR TO MONTH;
SELECT INTERVAL '-5 12:30:45' DAY TO SECOND;
SELECT toDateTime('2024-01-01 12:00:00') + INTERVAL '-1:30' HOUR TO MINUTE;
SELECT toDateTime('2024-01-01 01:00:01') + INTERVAL '-1:00:01' HOUR TO SECOND;
SELECT toDate('2026-09-01') + INTERVAL '-2-6' YEAR TO MONTH;
SELECT toDateTime('2024-01-06 12:30:45') + INTERVAL '-5 12:30:45' DAY TO SECOND;

SELECT '-- Leading plus sign';
SELECT INTERVAL '+1:30' HOUR TO MINUTE;

SELECT '-- Interval + Interval';
SELECT INTERVAL '1:30' HOUR TO MINUTE + INTERVAL '0:15' HOUR TO MINUTE;

SELECT '-- Interval comparison';
SELECT INTERVAL '1:30' HOUR TO MINUTE = INTERVAL '1:30' HOUR TO MINUTE;
SELECT INTERVAL '1:30' HOUR TO MINUTE != INTERVAL '2:00' HOUR TO MINUTE;
SELECT INTERVAL '1:30' HOUR TO MINUTE <> INTERVAL '2:00' HOUR TO MINUTE;
SELECT INTERVAL '1:30' HOUR TO MINUTE < INTERVAL '2:00' HOUR TO MINUTE;

SELECT '-- Interval * integer (unsupported for intervals)';
SELECT INTERVAL '1:30' HOUR TO MINUTE * 2; -- { serverError ILLEGAL_TYPE_OF_ARGUMENT }

SELECT '-- Large leading field';
SELECT INTERVAL '1000000:59' HOUR TO MINUTE;
