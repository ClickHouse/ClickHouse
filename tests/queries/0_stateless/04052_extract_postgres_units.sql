-- Test PostgreSQL-compatible EXTRACT units

-- EPOCH: seconds since 1970-01-01
SELECT EXTRACT(EPOCH FROM toDateTime('2024-01-15 12:30:45', 'UTC'));
SELECT EXTRACT(EPOCH FROM toDate('2024-01-15'));
SELECT EXTRACT(EPOCH FROM toDateTime64('2024-01-15 12:30:45.123456', 6, 'UTC')); -- Note: returns integer seconds (subsecond part is truncated)

-- DOW: day of week, 0 = Sunday, 6 = Saturday
SELECT EXTRACT(DOW FROM toDate('2024-01-15')); -- Monday = 1
SELECT EXTRACT(DOW FROM toDate('2024-01-14')); -- Sunday = 0
SELECT EXTRACT(DOW FROM toDate('2024-01-20')); -- Saturday = 6

-- DOY: day of year
SELECT EXTRACT(DOY FROM toDate('2024-01-01')); -- 1
SELECT EXTRACT(DOY FROM toDate('2024-12-31')); -- 366 (leap year)
SELECT EXTRACT(DOY FROM toDate('2023-12-31')); -- 365

-- ISODOW: ISO day of week, 1 = Monday, 7 = Sunday
SELECT EXTRACT(ISODOW FROM toDate('2024-01-15')); -- Monday = 1
SELECT EXTRACT(ISODOW FROM toDate('2024-01-14')); -- Sunday = 7
SELECT EXTRACT(ISODOW FROM toDate('2024-01-20')); -- Saturday = 6

-- ISOYEAR: ISO 8601 week-numbering year
SELECT EXTRACT(ISOYEAR FROM toDate('2024-01-01')); -- 2024
SELECT EXTRACT(ISOYEAR FROM toDate('2020-12-31')); -- 2020

-- WEEK: ISO week number
SELECT EXTRACT(WEEK FROM toDate('2024-01-01')); -- 1
SELECT EXTRACT(WEEK FROM toDate('2024-12-30')); -- 1

-- CENTURY
SELECT EXTRACT(CENTURY FROM toDate('2024-01-01')); -- 21
SELECT EXTRACT(CENTURY FROM toDate('2000-06-15')); -- 20
SELECT EXTRACT(CENTURY FROM toDate('2001-01-01')); -- 21

-- DECADE
SELECT EXTRACT(DECADE FROM toDate('2024-01-01')); -- 202
SELECT EXTRACT(DECADE FROM toDate('1999-12-31')); -- 199

-- MILLENNIUM
SELECT EXTRACT(MILLENNIUM FROM toDate('2024-01-01')); -- 3
SELECT EXTRACT(MILLENNIUM FROM toDate('2000-06-15')); -- 2
SELECT EXTRACT(MILLENNIUM FROM toDate('2001-01-01')); -- 3

-- Case-insensitive
SELECT EXTRACT(epoch FROM toDateTime('2024-01-15 12:30:45', 'UTC'));
SELECT EXTRACT(dow FROM toDate('2024-01-15'));
SELECT EXTRACT(isodow FROM toDate('2024-01-15'));

-- DateTime64 with other units
SELECT EXTRACT(DOW FROM toDateTime64('2024-01-15 12:30:45.123', 3));
SELECT EXTRACT(DOY FROM toDateTime64('2024-01-15 12:30:45.123', 3));
SELECT EXTRACT(ISOYEAR FROM toDateTime64('2024-01-15 12:30:45.123', 3));

-- Date32
SELECT EXTRACT(EPOCH FROM toDate32('2024-01-15'));
SELECT EXTRACT(DOW FROM toDate32('2024-01-15')); -- Monday = 1
SELECT EXTRACT(DOY FROM toDate32('2024-01-15')); -- 15
SELECT EXTRACT(ISODOW FROM toDate32('2024-01-14')); -- Sunday = 7
SELECT EXTRACT(ISOYEAR FROM toDate32('2024-01-01')); -- 2024
SELECT EXTRACT(WEEK FROM toDate32('2024-01-01')); -- 1
SELECT EXTRACT(CENTURY FROM toDate32('2024-01-01')); -- 21
SELECT EXTRACT(DECADE FROM toDate32('2024-01-01')); -- 202
SELECT EXTRACT(MILLENNIUM FROM toDate32('2024-01-01')); -- 3

-- DateTime64 with all new units
SELECT EXTRACT(EPOCH FROM toDateTime64('2024-01-15 12:30:45.999', 3, 'UTC'));
SELECT EXTRACT(DOW FROM toDateTime64('2024-01-14 00:00:00', 0)); -- Sunday = 0
SELECT EXTRACT(DOY FROM toDateTime64('2024-12-31 23:59:59.999', 3)); -- 366
SELECT EXTRACT(ISODOW FROM toDateTime64('2024-01-14 12:00:00', 0)); -- Sunday = 7
SELECT EXTRACT(ISOYEAR FROM toDateTime64('2020-12-31 23:59:59', 0)); -- 2020
SELECT EXTRACT(WEEK FROM toDateTime64('2024-12-30 12:00:00', 0)); -- 1
SELECT EXTRACT(CENTURY FROM toDateTime64('2000-06-15 00:00:00', 0)); -- 20
SELECT EXTRACT(DECADE FROM toDateTime64('1999-12-31 23:59:59', 0)); -- 199
SELECT EXTRACT(MILLENNIUM FROM toDateTime64('2000-06-15 00:00:00', 0)); -- 2
