SELECT 'contextual qualifier vs identifier';
SELECT 2 BETWEEN `symmetric` - 1 AND 20 FROM (SELECT 10 AS `symmetric`);
SELECT 2 BETWEEN `asymmetric` - 1 AND 20 FROM (SELECT 10 AS `asymmetric`);
