#!/usr/bin/env bash

set -e -o pipefail

ROOT_PATH="${1:-}"
if [[ -z "$ROOT_PATH" ]]
then
    ROOT_PATH="$(git rev-parse --show-toplevel)"
fi

GITMODULES="$ROOT_PATH/.gitmodules"
UPSTREAMS="$ROOT_PATH/utils/list-contribs/upstreams.tsv"

# Load the upstream manifest (submodule_url \t upstream_url \t notes), if present.
declare -A UPSTREAM_BY_URL
if [[ -f "$UPSTREAMS" ]]
then
    while IFS=$'\t' read -r submodule_url upstream_url _rest
    do
        [[ -z "$submodule_url" || "$submodule_url" == \#* ]] && continue
        UPSTREAM_BY_URL["$submodule_url"]="$upstream_url"
    done < "$UPSTREAMS"
fi

# Best-effort version: exact tag, then a ClickHouse/* remote branch containing HEAD, then `describe --tags --always`.
get_version()
{
    local dir="$1"
    if [[ ! -e "$dir/.git" ]]
    then
        return
    fi

    local exact_tag
    exact_tag=$(git -C "$dir" describe --tags --exact-match 2>/dev/null || true)
    if [[ -n "$exact_tag" ]]
    then
        echo "$exact_tag"
        return
    fi

    local clickhouse_branch
    clickhouse_branch=$(git -C "$dir" branch -r --contains HEAD 2>/dev/null \
        | sed -e 's/^[[:space:]]*//' -e 's|^origin/||' \
        | grep -m1 '^ClickHouse/' || true)
    if [[ -n "$clickhouse_branch" ]]
    then
        echo "$clickhouse_branch"
        return
    fi

    git -C "$dir" describe --tags --always 2>/dev/null || true
}

# (path, url) pairs for git submodules under contrib/, excluding rust_vendor.
PATHS_AND_URLS=$(
    git config -f "$GITMODULES" --get-regexp '^submodule\..*\.path$' \
        | while read -r key submodule_path
        do
            section="${key%.path}"
            submodule_url=$(git config -f "$GITMODULES" --get "${section}.url" || true)
            [[ -z "$submodule_url" ]] && continue
            [[ "$submodule_path" != contrib/* ]] && continue
            [[ "$submodule_path" == contrib/rust_vendor/* ]] && continue
            printf '%s\t%s\n' "$submodule_path" "$submodule_url"
        done \
        | LC_ALL=C sort
)

echo "// autogenerated by $0"
echo "const char * library_contribs[] = {"

while IFS=$'\t' read -r submodule_path submodule_url
do
    [[ -z "$submodule_path" ]] && continue
    name=$(basename "$submodule_path")

    commit=$(git -C "$ROOT_PATH" ls-files --stage -- "$submodule_path" 2>/dev/null \
        | awk '$1 == "160000" { print $2; exit }')
    if [[ -z "$commit" ]]
    then
        continue
    fi

    upstream_url="${UPSTREAM_BY_URL[$submodule_url]:-}"

    is_fork=0
    if [[ "$submodule_url" == https://github.com/ClickHouse/* ]]
    then
        is_fork=1
    fi
    if [[ -n "$upstream_url" && "$upstream_url" != "$submodule_url" ]]
    then
        is_fork=1
    fi

    # Non-forks with no explicit manifest entry: the submodule URL is the upstream.
    if [[ -z "$upstream_url" && "$is_fork" == "0" ]]
    then
        upstream_url="$submodule_url"
    fi

    version=$(get_version "$ROOT_PATH/$submodule_path")

    printf 'R"heredoc(%s)heredoc", R"heredoc(/%s)heredoc", R"heredoc(%s)heredoc", R"heredoc(%s)heredoc", R"heredoc(%s)heredoc", "%d", R"heredoc(%s)heredoc",\n\n' \
        "$name" "$submodule_path" "$commit" "$submodule_url" "$upstream_url" "$is_fork" "$version"
done <<< "$PATHS_AND_URLS"

echo "nullptr"
echo "};"
