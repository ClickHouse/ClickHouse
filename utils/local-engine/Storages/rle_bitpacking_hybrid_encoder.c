int numberOfLeadingZeros(uint32_t v) 
{
    // HD, Figure 5-6
    if (v == 0)
        return 32;
    int n = 1;
    if (v >> 16 == 0) { n += 16; v <<= 16; }
    if (v >> 24 == 0) { n +=  8; v <<=  8; }
    if (v >> 28 == 0) { n +=  4; v <<=  4; }
    if (v >> 30 == 0) { n +=  2; v <<=  2; }
    n -= v >> 31;
    return n;
}


int getWidthFromMaxInt(uint32_t bound) 
{
    return 32 - numberOfLeadingZeros(bound);
}


int writeUnsignedVarInt(uint32_t value, uint8_t *out)
{
    int i = 0;
    while ((value & 0xFFFFFF80) != 0) {
        out[i++] = (value & 0x7F) | 0x80;
        value >>= 7;
    }
    out[i++] = value & 0x7F;
    return i;
}


uint32_t readUnsignedVarInt(uint8_t *out, int out_len)
{
    uint32_t v = 0;
    int i;
    for (i = out_len - 1; i >= 0; i--) {
        v <<= 7;
        v += out[i] & 0x7F;
    }
    return v;
}


int writeIntLittleEndianPaddedOnBitWidth(uint32_t v, int bitWidth, uint8_t *out)
{
    int n = (bitWidth + 7) / 8;
    switch (n) {
      case 0:
        break;
      case 1:
        out[0] = ((v >>  0) & 0xFF);
        break;
      case 2:
        out[0] = ((v >>  0) & 0xFF);
        out[1] = ((v >>  8) & 0xFF);
        break;
      case 3:
        out[0] = ((v >>  0) & 0xFF);
        out[1] = ((v >>  8) & 0xFF);
        out[2] = ((v >>  16) & 0xFF);
        break;
      case 4:
        out[0] = ((v >>  0) & 0xFF);
        out[1] = ((v >>  8) & 0xFF);
        out[2] = ((v >>  16) & 0xFF);
        out[3] = ((v >>  24) & 0xFF);
        break;
      default:
        break;
    }

    return n;
}


uint32_t readIntLittleEndianPaddedOnBitWidth(uint8_t *out, int out_len)
{
    uint32_t v = 0;
    int i;
    for (i = out_len - 1; i >= 0; i--) {
        v <<= 8;
        v += out[i];
    }
    return v;
}


