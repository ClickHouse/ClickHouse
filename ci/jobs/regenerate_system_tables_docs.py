"""Regenerate system tables documentation and open a PR if it changed.

Runs `utils/generate-system-tables-docs` against the ARM ClickHouse binary
(delivered as the `CH_ARM_BIN` artifact at `ci/tmp/clickhouse`, the same path
`docs_job.py` uses) to refresh the autogenerated column lists in
`docs/en/operations/system-tables/`. When the result differs from what is
committed, it opens -- or force-updates -- a single pull request against
master.

The C++ `getColumnsDescription()` is the single source of truth for column
descriptions; this job only propagates that into the Markdown between the
`<!--AUTOGENERATED_START-->` / `<!--AUTOGENERATED_END-->` markers, leaving
hand-written sections (examples, "See Also") untouched. Scheduled weekly by
`ci/workflows/regenerate_system_tables_docs.py`.
"""

import shlex
import subprocess

from praktika.result import Result
from praktika.utils import Shell, Utils

DOCS_DIR = "docs/en/operations/system-tables"
GENERATOR = "utils/generate-system-tables-docs"
BRANCH = "auto/regenerate-system-tables-docs"
TITLE = "Auto-regenerate system tables documentation"
# Team (or user) the opened PR requests review from. Team form is `org/slug`;
# the robot must have permission to request review from this team.
REVIEWERS = "ClickHouse/docs"
PR_BODY = """\
Automated regeneration of the system tables documentation from a ClickHouse \
binary (`system.tables` / `system.columns`).

The column lists between the `<!--AUTOGENERATED_START-->` / \
`<!--AUTOGENERATED_END-->` markers in `docs/en/operations/system-tables/` are \
produced by `utils/generate-system-tables-docs`. The C++ \
`getColumnsDescription()` is the single source of truth -- edit column \
descriptions there, not in the Markdown.

Generated by the `RegenerateSystemTablesDocs` workflow. Review the diff, then \
merge if the changes are intended.

### Changelog category (leave one):
- Documentation (changelog entry is not required)
"""


def run_generator():
    """Run the generator against the delivered ARM binary."""
    cwd = Utils.cwd()
    binary = f"{cwd}/ci/tmp/clickhouse"
    return Shell.check(
        f"python3 {cwd}/{GENERATOR}"
        f" --binary {binary}"
        f" --docs-dir {cwd}/{DOCS_DIR}/",
        verbose=True,
    )


def docs_changed():
    """True if the generator edited or added any files under DOCS_DIR.

    Uses `git status --porcelain` so newly created table pages (untracked
    files), not just edits to existing ones, are detected.
    """
    out = subprocess.run(
        ["git", "status", "--porcelain", "--", DOCS_DIR],
        capture_output=True,
        text=True,
    ).stdout.strip()
    print(f"Detected changes:\n{out}" if out else "No documentation changes detected.")
    return bool(out)


def open_or_update_pr():
    """Push the regenerated docs to a fixed branch and open a PR if none is open.

    A fixed branch means a weekly run force-updates the existing open PR instead
    of opening a new one each week. `gh` is available on the bare style-check
    runner and authenticated via the job's `enable_gh_auth`.
    """
    push = " && ".join(
        [
            f"git checkout -B {BRANCH}",
            f"git add {DOCS_DIR}",
            "git -c user.name='robot-clickhouse'"
            " -c user.email='robot-clickhouse@users.noreply.github.com'"
            f" commit -m {shlex.quote(TITLE)}",
            f"git push --force origin {BRANCH}",
        ]
    )
    if not Shell.check(push, verbose=True):
        return False

    open_count = subprocess.run(
        ["gh", "pr", "list", "--head", BRANCH, "--state", "open",
         "--json", "number", "--jq", "length"],
        capture_output=True,
        text=True,
    ).stdout.strip()
    if open_count and open_count != "0":
        print(f"Open PR already exists for {BRANCH}; force-push updated it.")
        return True

    return Shell.check(
        f"gh pr create --base master --head {BRANCH}"
        f" --title {shlex.quote(TITLE)} --body {shlex.quote(PR_BODY)}"
        f" --label pr-documentation --reviewer {REVIEWERS}",
        verbose=True,
    )


def regenerate_and_maybe_pr():
    if not docs_changed():
        return True
    return open_or_update_pr()


if __name__ == "__main__":
    results = []
    ok = True

    if ok:
        results.append(
            Result.from_commands_run(name="Run generator", command=run_generator)
        )
        ok = results[-1].is_ok()

    if ok:
        results.append(
            Result.from_commands_run(
                name="Open PR if changed", command=regenerate_and_maybe_pr
            )
        )
        ok = results[-1].is_ok()

    Result.create_from(results=results).complete_job()
