class VersionType:
    NEW = "new"
    TESTING = "testing"
    STABLE = "stable"
    LTS = "lts"
    VALID = {NEW, TESTING, STABLE, LTS}


class ClickHouseVersion:
    _TAG_PATTERN = re.compile(
        r"\Av\d{2}([.][1-9]\d*){3}-(new|testing|prestable|stable|lts)\Z"
    )
    _TWEAK_DEFAULT = 1

    def __init__(self, major, minor, patch, revision, tweak=None):
        self._major = int(major)
        self._minor = int(minor)
        self._patch = int(patch)
        self._revision = int(revision)
        self._tweak = int(tweak) if tweak is not None else self._TWEAK_DEFAULT
        self._description = ""
        self._describe = ""

    def bump(self) -> "ClickHouseVersion":
        """Bump minor version (or wrap to next major when minor reaches 12)."""
        if self._minor < 12:
            self._minor += 1
        else:
            self._major += 1
            self._minor = 1
        self._revision += 1
        self._patch = 1
        self._tweak = 1
        return self

    def bump_patch(self) -> "ClickHouseVersion":
        self._revision += 1
        self._patch += 1
        self._tweak = 1
        return self

    def reset_tweak(self) -> "ClickHouseVersion":
        self._tweak = 1
        return self

    def with_description(self, version_type: str) -> "ClickHouseVersion":
        if version_type not in VersionType.VALID:
            raise ValueError(f"version type {version_type!r} not in {VersionType.VALID}")
        self._description = version_type
        self._describe = f"v{self.string}-{version_type}"
        return self

    def get_stable_release_type(self) -> str:
        return VersionType.LTS if self._minor % 5 == 3 else VersionType.STABLE

    @property
    def major(self) -> int:
        return self._major

    @property
    def minor(self) -> int:
        return self._minor

    @property
    def patch(self) -> int:
        return self._patch

    @property
    def revision(self) -> int:
        return self._revision

    @property
    def tweak(self) -> int:
        return self._tweak

    @tweak.setter
    def tweak(self, value: int) -> None:
        self._tweak = value

    @property
    def description(self) -> str:
        return self._description

    @property
    def describe(self) -> str:
        return self._describe

    @property
    def string(self) -> str:
        return f"{self._major}.{self._minor}.{self._patch}.{self._tweak}"

    def as_dict(self) -> dict:
        return {
            "revision": self._revision,
            "major": self._major,
            "minor": self._minor,
            "patch": self._patch,
            "tweak": self._tweak,
            "githash": Shell.get_output("git rev-parse HEAD") or "0" * 40,
            "describe": self._describe,
            "string": self.string,
        }

    def __eq__(self, other) -> bool:
        if not isinstance(other, ClickHouseVersion):
            return NotImplemented
        return (self._major, self._minor, self._patch, self._tweak) == (
            other._major, other._minor, other._patch, other._tweak
        )

    def __lt__(self, other) -> bool:
        if not isinstance(other, ClickHouseVersion):
            return NotImplemented
        return (self._major, self._minor, self._patch, self._tweak) < (
            other._major, other._minor, other._patch, other._tweak
        )


class Git:
    """Minimal git helper using Shell; reads tags for version tweak computation."""

    _TAG_PATTERN = re.compile(
        r"\Av\d{2}([.][1-9]\d*){3}-(new|testing|prestable|stable|lts)\Z"
    )

    def __init__(self):
        self.latest_tag = Shell.get_output("git describe --tags --abbrev=0") or ""
        self.new_tag = ""
        self.commits_since_latest = 0
        self.commits_since_new = 0
        if self.latest_tag:
            self.commits_since_latest = int(
                Shell.get_output(
                    f"git rev-list {self.latest_tag}..HEAD --first-parent --count"
                )
                or 0
            )
            if self.latest_tag.endswith("-new"):
                self.new_tag = Shell.get_output(
                    f"git describe --tags --abbrev=0 --exclude='{self.latest_tag}'"
                ) or ""
                if self.new_tag:
                    self.commits_since_new = int(
                        Shell.get_output(
                            f"git rev-list {self.new_tag}..HEAD --first-parent --count"
                        )
                        or 0
                    )

    def _tweak_for(self, tag: str, commits: int) -> int:
        if not tag.endswith("-testing"):
            if commits:
                return commits
            version_part = tag.split("-", 1)[0]
            try:
                return int(version_part.split(".")[-1])
            except ValueError:
                return 1
        version_part = tag.split("-", 1)[0]
        return int(version_part.split(".")[-1]) + commits

    @property
    def tweak(self) -> int:
        return self._tweak_for(self.latest_tag, self.commits_since_latest)

    @property
    def tweak_to_new(self) -> int:
        if self.new_tag:
            return self._tweak_for(self.new_tag, self.commits_since_new)
        return 1


def _read_versions() -> dict:
    versions = {}
    for line in Path(FILE_WITH_VERSION_PATH).read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line.startswith("SET("):
            continue
        name, value = line[4:-1].split(maxsplit=1)
        name = name.removeprefix("VERSION_").lower()
        try:
            value = int(value)
        except ValueError:
            pass
        versions[name] = value
    return versions


def get_version_from_repo(git: Optional[Git] = None) -> ClickHouseVersion:
    v = _read_versions()
    version = ClickHouseVersion(v["major"], v["minor"], v["patch"], v["revision"])
    if git is not None and git.latest_tag:
        latest = _version_from_tag(git.latest_tag)
        if latest.description == VersionType.NEW and version < latest:
            version.tweak = git.tweak_to_new
        else:
            version.tweak = git.tweak
    return version


def _version_from_tag(tag: str) -> ClickHouseVersion:
    tag_body, description = tag[1:].split("-", 1)
    parts = tag_body.split(".")
    v = ClickHouseVersion(parts[0], parts[1], parts[2], -1, int(parts[3]))
    v.with_description(description)
    return v


def update_cmake_version(
    version: ClickHouseVersion, preserve_sha: bool = False
) -> None:
    version_dict = version.as_dict()
    if preserve_sha:
        version_dict["githash"] = _read_versions().get("githash", "0" * 40)
    Path(FILE_WITH_VERSION_PATH).write_text(
        VERSIONS_TEMPLATE.format_map(version_dict), encoding="utf-8"
    )


def update_contributors(raise_error: bool = False) -> None:
    is_shallow = (
        Shell.get_output("git rev-parse --is-shallow-repository") or ""
    ).strip()
    if is_shallow == "true":
        msg = "The repository is shallow, refusing to update contributors"
        if raise_error:
            raise RuntimeError(msg)
        print(f"WARNING: {msg}")
        return

    shortlog = Shell.get_output("git shortlog HEAD --summary") or ""
    escaping = str.maketrans({"\\": "\\\\", '"': '\\"'})
    contributors = sorted(
        [c.split(maxsplit=1)[-1].translate(escaping) for c in shortlog.splitlines()]
    )
    contributors_lines = [f'    "{c}",' for c in contributors]
    executer = Path(__file__).relative_to(Path.cwd())
    content = CONTRIBUTORS_TEMPLATE.format(
        executer=executer, contributors="\n".join(contributors_lines)
    )
    Path(GENERATED_CONTRIBUTORS).write_text(content, encoding="utf-8")


